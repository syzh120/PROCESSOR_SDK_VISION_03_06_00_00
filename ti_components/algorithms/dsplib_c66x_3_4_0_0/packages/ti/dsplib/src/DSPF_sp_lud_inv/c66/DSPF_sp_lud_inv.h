/** @defgroup  DSPF_sp_lud_inverse */
/** @ingroup DSPF_sp_lud_inverse */
/* @{ */

/**
 *      This function processes a permutation matrix P, a lower triangular matrix L, and an upper triangular
 *      matrix U generated by DSPF_sp_lud_inv and generates the inverse of the A matrix where inv_A=inv(U)*inv(L)*P.
 *      In place processing is performed on the L and U matrices such that their original contents are
 *      modified. The values stored in the matrices are assumed to be single precision floating point values.
 *      This code is suitable for dense matrices. No optimizations are made for sparse matrices.
 *
 *     @param  order       = order of matrix A
 *     @param  P           = pointer to permutation matrix P[order*order]
 *     @param  L           = pointer to lower triangular matrix L[order*order]
 *     @param  U           = pointer to upper triangular matrix U[order*order]
 *     @param  inv_A       = pointer to inverse of A matrix[order*order]
 *
 *
 * @par Algorithm:
 * DSPF_sp_lud_inverse.c is the natural C equivalent of the optimized intrinsic
 * C code without restrictions. Note that the intrinsic C code is optimized
 * and restrictions may apply.
 *
 * @par Assumptions:
 *  The arrays P, L, U, and inv_A are stored in distinct arrays. In-place processing of L and U is done. <BR>
 *
 * @par Implementation Notes:
 * @b Interruptibility : The code is interruptible.  <BR>
 * @b Endian support   : supports both Little and Big endian modes.  <BR>
 *
 */

int DSPF_sp_lud_inverse(const int order,unsigned short *restrict P,float *restrict L,float *restrict U,float *restrict inv_A);

/* @} */ /* ingroup */
/* @} */ /* ingroup */

