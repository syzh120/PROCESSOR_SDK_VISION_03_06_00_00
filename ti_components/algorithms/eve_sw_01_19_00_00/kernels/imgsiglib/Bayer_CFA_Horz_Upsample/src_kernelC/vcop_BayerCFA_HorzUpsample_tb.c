int verify_output()
{
    int i, j, ok;

    ok = 1;

    for ( j = 0; j < OUT_H; j++)
    {
       for ( i = 0; i < OUT_W; i++)
       {
           if (out_m[j * OUT_W + i] != ref[j * OUT_W + i])
               ok = 0;
       }
    }

    return (ok);
}


