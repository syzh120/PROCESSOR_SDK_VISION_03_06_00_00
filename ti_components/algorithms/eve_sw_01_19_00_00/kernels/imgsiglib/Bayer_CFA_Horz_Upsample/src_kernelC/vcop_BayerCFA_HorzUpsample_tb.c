int main()
{
    int   i;
    int   ok;
    int   NUM_TAPS = 0;
    int   ignore_size = 0;
    unsigned char ignore[2] = {0};

#ifdef REPORT
    FILE* fpReport = fopen("../../../report.csv","a+");
    unsigned int exec_diff;
    #ifdef SCTM
    unsigned int sctm_t1, sctm_t2;
    unsigned int overflow;
    EVE_SCTM_Enable(CTM);
    EVE_SCTM_CounterConfig(CTM, SCTM_Counter_0, VCOP_BUSY, SCTM_DURATION);
    EVE_SCTM_CounterTimerEnable(CTM, SCTM_Counter_0);
    #endif
#endif

#ifdef CONSOLE
    printf("\nvcop_BayerCFA_HorzUpsample:\n");
#endif

    /*-------------------------------------------------------------------*/
    /* Compute the coefficients for CFA Horizontal Upsampling.           */
    /*-------------------------------------------------------------------*/
    NUM_TAPS = GenResamplingCoeffs(UP_SAMPLE, 1, TOTAL_TAPS, fir_coefs, ignore, &ignore_size);

#ifdef CONSOLE

    printf("\nUpsampling value = %d\n\n", UP_SAMPLE);
    printf("\nNumber of coefficients per phase of polyphase (NUM_TAPS) computed is: %d\n\n", NUM_TAPS);

    printf("\n--- COEF DATA ---\n\n");
    for ( i = 0; i < NUM_TAPS * UP_SAMPLE; i++)
    {
        printf("%4d, ", fir_coefs[i]);

    }
    printf("\n\n");

#endif

    /*-------------------------------------------------------------------*/
    /* Now doing compiler assisted memory allocation for intermediate    */
    /* output 'out' and final output 'out_final' buffers.                */
    /*-------------------------------------------------------------------*/
#ifdef CONSOLE
    printf("Now allocating memory. . .\n");
#endif
    vcop_setview(VCOP_MEMALIASED);

#if VCOP_HOST_EMULATION
    out_m           = (unsigned short *) malloc(sizeof(unsigned short) * COMP_W * COMP_H * (UP_SAMPLE + 1));
#else
    out_m           = (unsigned short *) vcop_malloc(VCOP_WMEM, sizeof(unsigned short) * COMP_W * COMP_H * (UP_SAMPLE + 1));
#endif

    if (!out_m)          { printf("Memory not allocated to output array! \n"); exit(1); }

    /*-------------------------------------------------------------------*/
    /* Actual filtering operation happens here as part of the test bench */
    /* and the results are written out so as to be compared with EVE     */
    /* results.                                                          */
    /*-------------------------------------------------------------------*/
#ifdef REPORT
    #ifdef SCTM
         sctm_t1 = EVE_SCTM_CounterRead (CTM, SCTM_Counter_0);
    #endif
#endif
    vcop_BayerCFA_HorzUpsample_ushort_short_ushort
    (
         in_data,
         fir_coefs,
         out_m,
         IN_W,
         NUM_TAPS,
         COMP_W,
         COMP_H,
         OUT_W,
         UP_SAMPLE,
         OUTPUT_TYPE,
         RND_SAT
    );
#ifdef REPORT
    #ifdef SCTM
        sctm_t2 = EVE_SCTM_CounterRead (CTM, SCTM_Counter_0);
        overflow= EVE_SCTM_OverflowCheck(CTM,SCTM_Counter_0);
        assert(overflow!=1);

        exec_diff = sctm_t2 - sctm_t1;
        EVE_SCTM_CounterTimerDisable(CTM, SCTM_Counter_0);
    #endif
#endif

#ifdef CONSOLE
    printf(". . . Done processing \n");
#endif

    /*-------------------------------------------------------------------*/
    /* Here we write out the output as a PGM to visualize the result.    */
    /*-------------------------------------------------------------------*/

    //write_pgm_file(UP_SAMPLE, 1, NUM_TAPS);

#ifdef CONSOLE
    //print_output();
#endif
    ok = verify_output();
#ifdef CONSOLE
    if (ok) printf("\nCFA Horizontal Upsampling kernel-C-code : PASS\n\n");
    else  printf("\nCFA Horizontal Upsampling kernel-C-code : FAIL\n\n");
#endif

    /*-------------------------------------------------------------------*/
    /* Now free the allocated memory.                                    */
    /*-------------------------------------------------------------------*/
 #ifdef CONSOLE
    printf("Now freeing memory \n");
#endif

#if VCOP_HOST_EMULATION
    free(out_m);
#else
    vcop_free(out_m);
#endif

#ifdef REPORT
    fprintf(fpReport,"vcop_BayerCFA_HorzUpsample,");
    fprintf(fpReport,"%d,",IN_W);
    fprintf(fpReport,"%d,",IN_H);
    fprintf(fpReport,"%d,",COMP_W);
    fprintf(fpReport,"%d,",COMP_H);
    fprintf(fpReport,"1,");
    fprintf(fpReport, ok ? "PASS," : "FAIL,");
    fprintf(fpReport,"%d\n",exec_diff);

    if(fpReport) {
        fclose(fpReport);
    }
#endif

    return 0;
}

void write_pgm_file
(
   int up_SAMPLE,
   int down_SAMPLE,
   int NUM_TAPS
)
{
    int i, j;
    char  filename[80] = {0};
    FILE  *fp;

    /*-------------------------------------------------------------------*/
    /* Generate filenames for output images to be written as PGM file.   */
    /*-------------------------------------------------------------------*/

    sprintf(filename, "C_ResampledImage_%dU_%dtaps.pgm", up_SAMPLE, NUM_TAPS);

    printf("Filename = %s\n", filename);
    fp = fopen(filename, "wb");

    if (fp == NULL) printf("Cannot open output file to write\n\n");

    fprintf(fp, "P5\n%d %d\n255\n", OUT_W, OUT_H);
    printf("%d %d\n", OUT_W, OUT_H);

    for ( j = 0; j < OUT_H; j++)
    {
        for ( i = 0; i < OUT_W; i++)
        {
            fwrite(&out_m[ (j * OUT_W) + i ], 1, 1, fp);
        }
    }

    fclose(fp);

    return;
}

