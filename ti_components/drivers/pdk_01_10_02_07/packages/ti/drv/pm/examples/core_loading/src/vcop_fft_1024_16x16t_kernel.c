volatile int loop = 50;
void start_fft() {
    int i = loop;
    VCOP_BUF_SWITCH_SET(WBUF_VCOP, IBUFHB_SYST, IBUFLB_SYST,
                   IBUFHA_VCOP, IBUFLA_VCOP);
   while(i){
   vcop_fft_1024_16x16t_stage_1_to_2_vloops(
    __pblock_vcop_fft_1024_16x16t_stage_1_to_2);
   vcop_fft_1024_16x16t_stage3_vloops(__pblock_vcop_fft_1024_16x16t_stage3);
   vcop_fft_1024_16x16t_stage4_vloops(__pblock_vcop_fft_1024_16x16t_stage4);
   vcop_fft_1024_16x16t_stage5_vloops(__pblock_vcop_fft_1024_16x16t_stage5);
   i--;
  }
   _vcop_vloop_done();
   VCOP_BUF_SWITCH_SET(WBUF_SYST, IBUFHB_SYST, IBUFLB_SYST,
                   IBUFHA_SYST, IBUFLA_SYST);
}

