short *pxi1[2] = {&in[2*256],  &in[2*64]  };
short *pxi2[2] = {&in[2*512],  &in[2*128] };
short *pxi3[2] = {&in[2*768],  &in[2*192] };

/*---------------------------------------------------------------------------*/
/*  Twiddle factors, are stored as 3 inputs per butterfly sequentially.      */
/*                                                                           */
/*  First stage: 0,     8,  16  3 twiddle factors per bfly, 256 bfys(N/4)    */
/*  Secd  stage: 256, 264, 272  3 twiddle factors per bfly, 64  bfys(N/16)   */
/*  Third stage: 320, 328, 336  3 twiddle factors per bfly, 16  bfys(N/64)   */
/*  Fourth stage: 336,344, 352  3 twiddle factors per bffly, 4  bfys(N/256)  */
/*                                                                           */
/*   2 (SC) * 3 (TWF per BFLY) * (352) + 8 PAD = 2120                        */
/*---------------------------------------------------------------------------*/

short *cs1[5]  = {&SC_vector[2*0],  &SC_vector[2*3*256]        };
short *cs2[5]  = {&SC_vector[2*8],  &SC_vector[2*3*256 + 2*8]  };
short *cs3[5]  = {&SC_vector[2*16], &SC_vector[2*3*256 + 2*16] };

/*---------------------------------------------------------------------------*/
/* In the Parameters for 1st stage, we perform 1 set of 256 4-point          */
/* FFT's. Since the vector core has two operation slots, each of             */
/* which works on eight data points, we work on 8 radix-4 bflies             */
/* in parallel. Therefore we only need one level of loop, which              */
/* will iterate 256/8 = 32 times, so loop counter is set up to be            */
/* 32 times.                                                                 */ 
/*                                                                           */
/*---------------------------------------------------------------------------*/

/*---------------------------------------------------------------------------*/
/* In the first stage of the radix-4 FFT, we point the data at inp.          */
/* offsets of 0, N/4, N/2 and 3 N/4 which for N = 1024 maps to               */
/* 0, 1024, 2048, 3072 in byte offsets, as each offset is 4 bytes            */
/* corresponding to 2 bytes real, 2 bytes imaginary. After we                */
/* calculate the radix-4 butterfly we write the output at the                */
/* same locations. Since we load 8 complex inputs from each of               */
/* the legs of the butterfly, input increment is 32 bytes. We                */
/* maintain twiddle factors in sets of 8, for the three inputs,              */
/* (2*pi*n*k)/N, (4*pi*n*k/N) and (6*pi*N*k)/N, so we jump by 96             */
/* bytes. Since we only have one level of loop, outer level loop             */
/* increments are set to 0. The shift by 15 as twiddle factors               */
/* are in Q15 is done by the store out. This is also why twiddle             */
/* factors for the three legs are set to be 8 complex values                 */
/* apart at 0, 32 and 64.                                                    */
/*---------------------------------------------------------------------------*/

/*---------------------------------------------------------------------------*/
/* In the second stage, we work on 4 sets of 64-point FFT's. So, we          */
/* set inner loop counter to be 64/8 = 8, and outer loop                     */
/* counter to be 4. We set input pointers to be at 0, 64,                    */
/* 128 and 192 which corresponds to byte offsets of 0, 256, 512              */
/* and 768. These are also the output offsets. The 256 radix-4               */
/* FFT's each use 3 twiddle factors, so we offset by 3 * 256 * 4             */
/* oe 3072. For every iteration of the inner loop, we increment by           */
/* 8 complex inputs or 32 bytes. In the next level of the inner              */
/* loop we want to advance to x[256] which is an offset of 1024.             */
/* However, since we have already advanced by 7 * 32 = 224, the              */
/* remaining offset is only 800 bytes. We re-use the twiddle                 */
/* factor values for all 4 sets of 64-point FFT's, hence we                  */
/* rewind by 7 times, each iteration's increment of 96 for an                */
/* overall increment of (-7 * 96) = -672. We achieve the rounding            */
/* leveraging the store path using Pby_15.                                   */
/*---------------------------------------------------------------------------*/

 
uint16_t bfys_per_grp[2] = {32,    8  };
uint16_t grps[2]         = {1,     4  };
uint16_t pinc3[2]        = {0,  1024  };
uint16_t ptnc3[2]        = {0,    0   };
uint16_t pinc4[2]        = {32,   32  };
uint16_t ptnc4[2]        = {96,   96  };

